  <h1>   PREGUNTAS MÁS FRECUENTES (FAQS)</h1>
  <div id="contenedor_texto">
    <h2>¿Se admiten animales?</h2>
      <p> Como grandes amantes de la naturaleza y de los animales que somos, en nuestro camping 
          las mascotas están permitidas, excepto en las zonas comunes (menos en recepción).
      </p>
    </h2>
    <h2>¿Puedo reservar un bungalow/tienda en concreto?</h2>
      <p> Actualmente, debido a la situación sanitaria las reservas están cerradas. Por otro lado,
          si desea reservar un bungalow o tienda en concreto, primero deberá contactar con nuestro 
          personal del camping para comprobar la disponibilidad de éste en las fechas deseadas.
      </p>
    </h2>
    <h2>¿Se pueden hacer barbacoas?</h2>
      <p> Sí, siempre que no sea en el suelo; el camping dispone de varias zonas con barbacoas, 
        incluso una zona de ellas cubierta.
      </p>
    </h2>
    <h2>¿Pueden entrar menores en el Spa?</h2>
      <p> En julio y agosto. Mañanas de 11:00 a 12:00 y tardes de 15:30 a 17:30. Fuera de temporada 
          siempre que estén acompañados de un adulto y siempre que no este activo el Spa.
      </p>
    </h2>
    <h2>¿Hay una edad mínima para ir al camping?</h2>
      <p> No, pero es imprescindible que al menos uno de los campistas sea mayor de edad.
      </p>
    </h2>
    <h2>¿A qué hora se puede hacer la llegada y la salida del hotel?</h2>
      <p> La hora de llegada es a partir de las 15:00 h y la de salida hasta las 12:00 h.
      </p>
    </h2>
    <h2>¿Cual es el horario de recepcion?</h2>
      <p> Encontrarás una persona en recepción las 24 horas del día, y si no hay nadie, el 
          teléfono de las oficinas centrales está conectado 24/7.
      </p>
    </h2>
    <h2>¿Se puede fumar?</h2>
      <p> No, por ley todas las zonas comunes del camping, los bungalows, las caravanas y las tiendas son espacios no fumadores.
      </p>
    </h2>
   </div>
