  <h1>   PREGUNTAS MÁS FRECUENTES (FAQS)</h1>
  <div id="contenedor_texto">
    <h2><dd>¿Se admiten animales?</dd></h2>
      <p>
        <dd> <img style="float:right; margin:10px;" src="view/img/faqs.png" width=450; height=250/>
          Como grandes amantes de la naturaleza y de los animales que somos, en nuestro camping 
          las mascotas están permitidas, excepto en las zonas comunes (excepto en recepción y en la 
          zona del parque infantil).
        </dd>
      </p>
    </h2>
    <h2><dd>¿Puedo reservar un bungalow/tienda en concreto?</dd></h2>
      <p>
        <dd> 
          Actualmente, debido a la situación sanitaria las reservas están cerradas. Por otro lado,
          si desea reservar un bungalow o tienda en concreto, primero deberá contactar con nuestro 
          personal del camping para comprobar la disponibilidad de éste en las fechas deseadas.
        </dd>
      </p>
    </h2>
    <h2><dd>¿Se pueden hacer barbacoas?</dd></h2>
      <p> 
        <dd>
          Sí, siempre que no sea en el suelo; el camping dispone de varias zonas con barbacoas, 
          incluso una zona de ellas cubierta.
        </dd>
      </p>
    </h2>
    <h2><dd>¿Pueden entrar menores en el Spa?</dd></h2>
      <p> 
        <dd>
          En julio y agosto. Mañanas de 11:00 a 12:00 y tardes de 15:30 a 17:30. Fuera de temporada 
          siempre que estén acompañados de un adulto y siempre que no este activo el Spa.
        </dd>
      </p>
    </h2>
    <h2><dd>¿Hay una edad mínima para ir al camping?</dd></h2>
      <p> 
        <dd>
          No, pero es imprescindible que al menos uno de los campistas sea mayor de edad.
        </dd>
      </p>
    </h2>
    <h2><dd>¿A qué hora se puede hacer la llegada y la salida del camping?</dd></h2>
      <p> 
        <dd>
          La hora de llegada es a partir de las 15:00 h y la de salida hasta las 12:00 h.
        </dd>
      </p>
    </h2>
    <h2><dd>¿Cual es el horario de recepcion?</dd></h2>
      <p> 
        <dd>
          Encontrarás una persona en recepción las 24 horas del día. Sin embargo, si no encuentras anadie, el 
          teléfono de las oficinas centrales está conectado 24/7.
        </dd>
      </p>
    </h2>
    <h2><dd>¿Se puede fumar?</dd></h2>
      <p> 
        <dd>
          No, por ley todas las zonas comunes del camping, los bungalows, las caravanas y las tiendas son espacios no fumadores.
        </dd>
      </p>
