     <h3>Encargados de áreas infantiles con experincia en el área infantil.</h3>
     <p>
       La experiencia conseguida en los últimos años nos ha permitido conseguir que nuestro equipo se componga de personas 
       que siempre están a disposición de sus clientes. También están dispuestos a solucionar cualquier problema que estos 
       tengan durante su estancia en nuestro cámping. Son personas comprometidas con su trabajo y en el caso de los encargados 
       de las instalaciones infantiles además son personas con experiencia en el área infantil.
     </p>
     <h3>Formación de primeros auxilios para todo el personal.</h3>
     <p>
       Durante los años que llevamos en activo nos hemos dado cuenta de la necesidad de un encargado de mantenimiento que pueda 
       arreglar los desperfectos de las instalaciones de la forma más rápida posible. También nos hemos preocupado de la seguridad 
       tanto de las instalaciones como del cámping en sí. Formando a todo nuestro personal en materia de primeros auxilios y 
       llevándonos a contratar un servicio de seguridad. Además, si el cliente lo desea puede solicitar el servicio de una caja fuerte
       por si tuviese algo de valor que desease proteger.
     </p>
     <h3>Adaptación a las necesidades del cliente.</h3>
     <p>Estos años nos han ayudado a comprender que cada cliente puede tener necesidades distintas al resto. Por ello, nos ha llevado a 
       ampliar cada año la oferta de instalaciones y a adaptarnos a las necesidades que pueda tener cada cliente. Llevandonos a 
       comprender que debemos intentar que cada cliente se sienta como si estuviese en su propia casa.
